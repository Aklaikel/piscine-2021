int	ft_sqrt(int	nb)
{
	long int	i;
	long int	a;

	i = 1;
	a = nb;
	if (a < 0)
		return (0);
	while (i * i < a)
		i++;
	if (a == i * i)
		return (i);
	return (0);
}
